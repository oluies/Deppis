  * functional consistency tests in [[OqsSpec]]. Unlike those, these assert exact published
  * standards outputs on the DETERMINISTIC verification/decapsulation paths.
  *
  * Vectors are GENUINE NIST ACVP test vectors, copied verbatim (as hex) from the public
  * `usnistgov/ACVP-server` repository. They are public test data, not secrets.
  *
  * Source: https://github.com/usnistgov/ACVP-server
  *   commit 15c0f3deeefbfa8cb6cd32a99e1ca3b738c66bf0 (2026-04-16), file `gen-val/json-files/...`
  *
  *   - ML-KEM-768 decapsulation (FIPS 203): `ML-KEM-encapDecap-FIPS203/internalProjection.json`,
  *     tgId=5 (function=decapsulation, parameterSet=ML-KEM-768, testType=VAL). Decapsulation is
  *     deterministic: `ML-KEM.Decaps(dk, c)` yields exactly the shared secret `k`.
  *       * tcId=89  (reason "valid decapsulation")   -> k reproduced exactly.
  *       * tcId=86  (reason "modified ciphertext")   -> ML-KEM's IND-CCA2 implicit rejection returns
