  *
  *   - ML-DSA-65 verification (FIPS 204): `ML-DSA-sigVer-FIPS204/internalProjection.json`,
  *     tgId=3 (parameterSet=ML-DSA-65, signatureInterface=external, preHash=pure, externalMu=false),
  *     tcId=35 (empty context, testPassed=true). This is FIPS 204 external `Verify(pk, M, sig, ctx="")`
  *     -- the interface liboqs' `OQS_SIG_verify` implements for ML-DSA-65 (confirmed empirically:
  *     the "internal"/Verify_internal ACVP groups do NOT verify through this binding, the external
  *     empty-context group does). Verification is deterministic. A one-bit tamper must be rejected.
  *
  * FIPS *conformance* of the primitives themselves lives upstream in liboqs (which passes the full
  * NIST ACVP suite); these pin a single vector per primitive as an end-to-end regression guard that
  * our FFM binding drives the standardized functions correctly. */
